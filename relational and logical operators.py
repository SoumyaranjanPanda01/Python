# <, >, <=, >= = self explanatory
# == = compares the two operators and returns value as true or false
# != = not equals to

print(5<50)
print(5>50)
print(5<=5)
print(5>=50)
print(5==5)
print(5==50)
print(5!=50)
print(5!=5)

